    NSString *r = [self ToHex:red];
    NSString *g = [self ToHex:green];
    NSString *b = [self ToHex:blue];
    
    NSString *hexString = [NSString stringWithFormat:@"%@%@%@",r,g,b];
    return hexString;
}

-(NSString *)ToHex:(int)tmpid
{
    NSString *nLetterValue;
    NSString *str =@"";
    int ttmpig;
    for (int i = 0; i<9; i++) {
        ttmpig=tmpid%16;
        tmpid=tmpid/16;
        switch (ttmpig)
        {
            case 10:
                nLetterValue =@"A";break;
            case 11:
                nLetterValue =@"B";break;
            case 12:
                nLetterValue =@"C";break;
            case 13:
                nLetterValue =@"D";break;
            case 14:
                nLetterValue =@"E";break;
            case 15:
                nLetterValue =@"F";break;
            default:nLetterValue=[[NSString alloc]initWithFormat:@"%d",ttmpig];
                
        }
        str = [nLetterValue stringByAppendingString:str];
        if (tmpid == 0) {
            break;  
        }  
        
    }
    NSString *hexStr;
    if([str length]==1){
        hexStr = [NSString stringWithFormat:@"0%@",str];
    }
    else {
        hexStr = str;
    }
    return hexStr;
}

